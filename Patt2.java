class Patt2{
    public static void main(String[] arg)
    {
        int k=3;
        while(k>0)
        {
            int l=1;
            while(l<=k)
            {
                System.out.print(l);
                l=l+1;
            }
            System.out.print("\n");
            k=k-1;
        }
        int i=1;
        while(i<4)
        {
            int j=1;
            while(j<=i)
            {
                System.out.print(j);
                j++;
            }
            System.out.print("\n");
            i++;
        }


}
}