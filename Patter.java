class Patter{
    public static void main(String[] arg)
    {
        int i=1;
        while(i<5)
        {
            int j=1;
            while(j<=i)
            {
                System.out.print(j);
                j++;
            }
            System.out.print("\n");
            i++;

        }
    
        
        int k=3;
        while(k>0)
        {
            int l=1;
            while(l<=k)
            {
                System.out.print(l);
                l=l+1;
            }
            System.out.print("\n");
            k=k-1;

        }
    }
}
